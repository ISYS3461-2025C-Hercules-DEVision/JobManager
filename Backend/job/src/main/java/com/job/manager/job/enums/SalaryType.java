package com.job.manager.job.enums;

/**
 * Salary representation types for job posts
 */
public enum SalaryType {
    RANGE,       // e.g., "1000-1500 USD"
    ABOUT,       // e.g., "About 1000 USD"
    UP_TO,       // e.g., "Up to 2000 USD"
    FROM,        // e.g., "From 3000 USD"
    NEGOTIABLE   // Salary is negotiable
}
